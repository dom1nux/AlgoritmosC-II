public class LeerMostrarBucle {
    public static void main(String[] args) {
        int[] LeerMostrar = new int[10];
        for (int i = 0; i < 10; i++) {
            LeerMostrar[i] = i + 1;
        }

        for (int x = 0; x < 10; x++) {
            System.out.println(LeerMostrar[x] + "");
        }
    }
}


