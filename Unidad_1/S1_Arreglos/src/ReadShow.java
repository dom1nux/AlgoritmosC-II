public class ReadShow {
    public static void main(String[] args) {
        int[] readShow = new int[10];
        readShow[0] = 1;
        readShow[1] = 2;
        readShow[2] = 3;
        readShow[3] = 4;
        readShow[4] = 5;
        readShow[5] = 6;
        readShow[6] = 7;
        readShow[7] = 8;
        readShow[8] = 9;
        readShow[9] = 10;

        for (int i = 0; i < 10; i++) {
            System.out.println(readShow[i]);
        }
    }
}
